// Helper functions
export function getItemById(id, items = seedData) {
  return items.find(item => item.id === id)
}

export function getChildrenOf(parentId, items = seedData) {
  const parent = getItemById(parentId, items)
  if (!parent || !parent.childIds) return []
  return parent.childIds.map(id => getItemById(id, items)).filter(Boolean)
}

export function getParentsOf(childId, items = seedData) {
  return items.filter(item => item.childIds?.includes(childId))
}

export function getObjectives(items = seedData) {
  return items.filter(item => item.type === 'objective')
}

export function getTactics(items = seedData) {
  return items.filter(item => item.type === 'tactic')
}

export function getBestPractices(items = seedData) {
  return items.filter(item => item.type === 'bestPractice')
}

export function getSteps(items = seedData) {
  return items.filter(item => item.type === 'step')
}

// Get root items for Table View (Objectives)
export function getRootItems(items = seedData) {
  return getObjectives(items)
}

// Get all items in hierarchical order
export function getHierarchicalItems(items = seedData) {
  const result = []
  
  function addWithChildren(item, depth = 0) {
    result.push({ ...item, depth })
    if (item.childIds) {
      item.childIds.forEach(childId => {
        const child = getItemById(childId, items)
        if (child) {
          addWithChildren(child, depth + 1)
        }
      })
    }
  }
  
  getObjectives(items).forEach(obj => addWithChildren(obj, 0))
  
  return result
}

